package engine;

public class Bullet {
	public double x;
	public double y;
	public double width;
	public double height;
	public double angle;
	
	public Bullet(double xNew, double yNew, double widthNew, double heightNew, double angleNew) {

		x = xNew;
		y = yNew;
		width = widthNew;
		height = heightNew;
		angle = angleNew;
	}
	
}
